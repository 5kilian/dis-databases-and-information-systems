package de.estate.data;

public class Estate {

    protected int id;

    protected String city;

    protected int plz;

    protected String street;

    protected int number;

    protected int area;

    protected int maneges;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getPlz() {
        return plz;
    }

    public void setPlz(int plz) {
        this.plz = plz;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getArea() {
        return area;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public int getManeges() {
        return maneges;
    }

    public void setManeges(int maneges) {
        this.maneges = maneges;
    }
}
